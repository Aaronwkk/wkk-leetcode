// @lc code=start
/**
 * @param {string} s
 * @param {string} p
 * @return {number[]}
 */
var findAnagrams = function(s, p) {

  let ls = new Array(s.length).fill(0)
  let lp = new Array(p.length).fill(0)
  const len = p.length
  let ans = []

  for(let j = 0; j<p.length; j++){
    lp[p.charCodeAt(j) - 97] += 1
    ls[s.charCodeAt(j) - 97] += 1
  }
  if(lp.toString() === ls.toString()){
    ans.push(0)
  }
  for(let k = len; k<s.length; k++){
    ls[s.charCodeAt(k-len) - 97]--
    ls[s.charCodeAt(k) - 97]++
    if(lp.toString() === ls.toString()){
      ans.push(k-len+1)
    }
  }
  return ans
};
