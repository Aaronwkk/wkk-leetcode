var preorderTraversal = function(root) {
  let stack = new Array()
  let node = root
  let ans = []
  while(stack.length || node){
    while(node){
      ans.push(node.val)
      stack.push(node)
      node = node.left
    }
    node = stack.pop()
    node = node.right
  }
  return ans
};
